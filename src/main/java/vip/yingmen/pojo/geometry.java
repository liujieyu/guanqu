package vip.yingmen.pojo;

import java.io.Serializable;

public class geometry implements Serializable {
    public String x;
    public String y;

    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }

    public String getY() {
        return y;
    }

    public void setY(String y) {
        this.y = y;
    }
}
